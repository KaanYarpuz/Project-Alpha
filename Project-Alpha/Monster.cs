class Monster
{
    // fields
    int ID;
    string name;
    int Attack;
    int Health;

    // Constructor
    public Monster (int C_id, string C_Name, int C_Attack, int C_Health)
    {
        this.ID = C_id;
        this.name = C_Name;
        this.Attack = C_Attack;
        this.Health = C_Health;
    }
} 
